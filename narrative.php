                    <h2>5 Changes</h2>
                    <ol class="ordered-list">
                        <li>Change 1: Added a drop down menu to show which hikes would appear under each category on the main navigation. This contributed to making the navigation more overall consistent and the site cleaner, which the users made note of.</li>
                        <li>Change 2: Added photos and trail maps to each of the hikes, to distinguish between them and make each hike's page more specific. </li>
                        <li>Change 3: Changed the link to the UVM Outing Club so it would open a new window. Users mentioned that they wished they could have stayed on our site and just had this outside link appear in a new window, so that they could continue to browse our site. We followed suit on this for all outside links on the site after this usability test.</li>
                        <li>Change 4: Changed the Resources Page to feature links of outside resources rather than just general hiking tips. Users were confused since most Resources pages are conventionally used for this purpose and not for tips.</li>
                        <li>Change 5: We fixed around the Home Page with a new description block, put the tips for hiking under that description, changed the name of the site to something more specific "LoVermont" to "LoVermont Hiking Project"</li>
                        <br></br>
                        <br></br>
                        <br></br>
                        <li>Change 6(After Second Test): Added thumbnails of photos to include in the buttons to get to each hike page. This indicates clearly where the button goes and gives the user a glimpse of the mountain so they can chose one that looks appealing to them. </li>
                        <li>Change 7(After Second Test): Created hover effects for the drop down nav menu, including rounded edges and lighter highlighting on the menu options. </li>

                    </ol>
